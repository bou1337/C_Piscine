int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	x;

	if (nb <= 0)
		return (0);
	x = nb;
	i = 1;
	while (i * i <= x)
	{
		if (x == i * i)
			return (i);
		i++;
	}
	return (0);
}
