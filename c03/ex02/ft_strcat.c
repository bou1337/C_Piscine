char	*ft_strcat(char *dest, char *src)
{
	char	*p1;

	p1 = dest;
	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (p1);
}
