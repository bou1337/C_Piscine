char	*ft_strstr(char *str, char *to_find)
{
	char	*p1;
	char	*p2;

	if (*to_find == '\0')
	{
		return (str);
	}
	while (*str != '\0')
	{
		p1 = str;
		p2 = to_find;
		while (*p1 == *p2 && *p2 != '\0')
		{
			p1++;
			p2++;
		}
		if (*p2 == '\0')
		{
			return (str);
		}
		str++;
	}
	return (0);
}
