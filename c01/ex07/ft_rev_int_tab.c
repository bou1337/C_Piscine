void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	change ;

	i = 0 ;
	j = size -1;
	while (i < j)
	{
		change = *(tab + i);
		*(tab + i) = *(tab + j);
		*(tab + j) = change;
		i ++;
		j --;
	}
}
